new Schema("farmersdelight:cutting")
	.simpleKey("result", "outputItemArray")
	.simpleKey("ingredients", "inputItemArray")
	.dynamicKey((components, componentsBuilder) =>
		components.get("inputItem")()
			.or(new componentsBuilder(2)
				.add(components.get("nonBlankString")().key("type"))
				.add(components.get("nonBlankString")().key("action"))
				.inputRole())
			.key("tool"))

